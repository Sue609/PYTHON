numbers = [4, 10, 5, 9, 2, 7]
numbers.append(20) #adds a digit at the end of the list
print(numbers)

numbers.insert(3, 23) #inserts a number at a given index
print(numbers)

numbers.remove(5) #removes a number from the list
print(numbers)

numbers.clear() #clears all the numbers from the list
print(numbers)

numbers.pop() #removes the last number or digit
print(numbers)

